void solve()
{

    ll n;
    cin >> n;
    string s;
    cin >> s;
    ll count1 = count(all(s), '1');
    ll count0 = 0;
    for (ll i = 0; i < n; i++)
    {
        if (s[i] == '1')
        {
            i++;
            continue;
        }
        else
        {
            ll j = i;
            count0 = 0;
            for (j = i; j < n; j++)
            {
                if (s[j] == '0')
                {
                    count0++;
                }
                else
                {
                    count0--;
                    i = j - 1;
                    break;
                }
            }
            if (count0 > 0)
            {
                count1 += ceil(count0 / 3.0);
                count0 = 0;
            }

            if (i != j - 1)
            {
                i = j;
            }
        }
    }
    if (count0 > 0)
    {
        count1 += ceil(count0 / 3.0);
    }
    out(count1);
}

